
def add(x, y):
    ''' 
	Function to add two numbers.
	Function then returns the result.
    '''

    result=(x+y)
    return('Sum of two numbers is :', result)

def subtract(x, y):
    ''' 
	Function to subtract two numbers.
	Function then returns the result.
    '''

    result=(x-y)
    return('Difference of two numbers is :', result)

def multiply(x, y):
    ''' 
	Function to multiply two numbers.
	Function then returns the result.
    '''

    result=(x*y)
    return('Result of multiplication is :', result)

def divide(x, y):
    ''' 
	Function to divide two numbers.
	Function then returns the result.
    '''

    result=(x/y)
    return('Result of division is :', result)

