def add(x,y):
    '''
      Function to add two numbers and return result.
    '''
    result = x+y
    return ("Sum of two numbers is :", result)

def subtract(x,y):
    '''
      Function to subtract two numbers and return result.
    '''
    result = x-y
    return ("Difference of two numbers is :", result)

def multiply(x,y):
    '''
      Function to multiply two numbers and return result.
    '''
    result = x*y
    return ("Product of two numbers is :", result)

def divide(x,y):
    '''
      Function to divide two numbers and return result.
    '''
    result = x/y
    return ("Ratio of two numbers is :", result)

